let currentIndex = 0;
let audio = document.getElementById("audio");
let songList = document.getElementById("allSongs");
let progressBar = document.getElementById("seekRange");


// 🎵 LOAD SONGS
function displaySongs(songArray) {
    songList.innerHTML = "";

    songArray.forEach((song) => {
        let div = document.createElement("div");
        div.classList.add("song-card");

        div.innerHTML = `
            <img src="${song.cover}">
            <p>${song.name}</p>
        `;

        div.onclick = () => playSong(songs.indexOf(song));
        songList.appendChild(div);
    });
}


// ▶️ PLAY SONG
function playSong(index) {
    currentIndex = index;
    audio.src = songs[index].file;
    audio.play();

    document.getElementById("playBtn").innerText = "⏸";

    // SWITCH VIEW
    document.getElementById("homeView").classList.add("hidden");
    document.getElementById("playerView").classList.remove("hidden");
    document.getElementById("playerView").classList.add("active");

    // UPDATE UI
    document.getElementById("mainCover").src = songs[index].cover;
    document.getElementById("mainTitle").innerText = songs[index].name;
    document.getElementById("trackArtist").innerText = songs[index].artist;
    document.getElementById("lyricsText").innerText = songs[index].lyrics || "No lyrics available";

    loadQueue();
}


// ⏯ PLAY / PAUSE
function togglePlay() {
    let playBtn = document.getElementById("playBtn");

    if (audio.paused) {
        audio.play();
        playBtn.innerText = "⏸";
    } else {
        audio.pause();
        playBtn.innerText = "▶";
    }
}


// ⏭ NEXT / PREV
function nextSong() {
    currentIndex = (currentIndex + 1) % songs.length;
    playSong(currentIndex);
}

function prevSong() {
    currentIndex = (currentIndex - 1 + songs.length) % songs.length;
    playSong(currentIndex);
}


// 📋 QUEUE
function loadQueue() {
    let queue = document.getElementById("queueList");
    queue.innerHTML = "";

    songs.forEach((song, i) => {
        if (i !== currentIndex) {
            let div = document.createElement("div");
            div.classList.add("queue-card");

            div.innerHTML = `<p>${song.name}</p>`;
            div.onclick = () => playSong(i);

            queue.appendChild(div);
        }
    });
}


// ⏱ PROGRESS BAR
audio.addEventListener("timeupdate", () => {
    if (audio.duration) {
        let progress = (audio.currentTime / audio.duration) * 100;
        progressBar.value = progress;

        document.getElementById("currentTime").innerText = formatTime(audio.currentTime);
        document.getElementById("duration").innerText = formatTime(audio.duration);
    }
});

progressBar.addEventListener("input", () => {
    if (audio.duration) {
        audio.currentTime = (progressBar.value / 100) * audio.duration;
    }
});


// ⏲ FORMAT TIME
function formatTime(time) {
    if (isNaN(time)) return "0:00";

    let min = Math.floor(time / 60);
    let sec = Math.floor(time % 60);

    return min + ":" + (sec < 10 ? "0" + sec : sec);
}


// 🔍 SEARCH (FIXED FOR BOTH INPUTS)
function searchSongs() {
    let input1 = document.getElementById("searchInput");
    let input2 = document.getElementById("topSearch");

    let value = "";

    if (input1 && input1.value) value = input1.value.toLowerCase();
    if (input2 && input2.value) value = input2.value.toLowerCase();

    let filtered = songs.filter(song =>
        song.name.toLowerCase().includes(value)
    );

    displaySongs(filtered);
}


// 🔙 BACK TO HOME
function goBack() {
    document.getElementById("playerView").classList.remove("active");
    document.getElementById("playerView").classList.add("hidden");
    document.getElementById("homeView").classList.remove("hidden");

    audio.pause();
}


